// Standard I/O : Input-Output
#include <stdio.h>

// Il nome della funzione "main" è importante
// SI : main
//  NO: Main, mAin, ...

// Definizione di una funzione
//[tipo di ritorno] [nome]([parametri])
// tipo di ritorno = int
// nome = main
// parametri = void: vuol dire che non ci sono parametri
int main(void)
{
    // Un'istruzione è terminata con il punto e virgola (;)
    printf("Hello World\n");

    // una chiamata a funzione è del tipo
    //  nome_funzione(...);

    return 0;
}