#include <stdio.h>

int main()
{

    // Il ciclo for serve per eseguire un numero definito di volte il codice
    //in questo casio viene eseguito per 10 volte

    //il for ha la seguente struttura:
    // for ( dichiarazione variabili; condizione; operazioni)
    //{
    //
    //}
    for (int i = 0; i < 10; i++)
    {
        printf("Stampo %d\n", i);
    }

    return 0;
}